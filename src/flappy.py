"""
Genetic algorithm solution to the constant-pipes flappy bird problem as outlined in method B in the report.
This is based on an approach with no crossover, wait-to-jump genome encoding and aggressive brittle hyperparameters
that use a stuck condition to become more robust. This presented version has what were the best hyperparameters found.
Presented iteration generates random pipes and keeps them constant with each game iteration on the same genetic
algorithm.

To achieve better performance in terms of convergence time, graphics can be removed by commenting out the relevant
instructions.

The code consists of a flappy.py main script with the game and some genetic algorithm quantities. This relies heavily on
external functions implemented in ga_utils.py

Christian D'Amico - 31/01/2020
"""

